<h2>ISQM Areas</h2>

<p>The ISQM Quality Management System covers six ISQM areas. This guide explains each area and how to work with them in the system.</p>

<h3>ISQM Areas Overview</h3>
<p>The system supports all six ISQM areas:</p>
<ul>
  <li>Governance & Leadership</li>
  <li>Ethical Requirements</li>
  <li>Acceptance & Continuance</li>
  <li>Engagement Performance</li>
  <li>Resources</li>
  <li>Information & Communication</li>
</ul>

<h3>Accessing ISQM Area Pages</h3>
<div class="step-box">
  <h4>Step 1: Navigate to ISQM Areas</h4>
  <p>Click on "ISQM Areas" in the main navigation sidebar.</p>
</div>

<div class="step-box">
  <h4>Step 2: Select an Area</h4>
  <p>Click on the area you want to view:
    <ul>
      <li>Governance & Leadership</li>
      <li>Ethical Requirements</li>
      <li>Acceptance & Continuance</li>
      <li>Engagement Performance</li>
      <li>Resources</li>
      <li>Information & Communication</li>
    </ul>
  </p>
</div>

<h3>Working with ISQM Areas</h3>

<h4>Creating Entries for an Area</h4>
<div class="step-box">
  <h4>Step 1: Select Area</h4>
  <p>When creating a new entry, select the ISQM area from the "Area" dropdown.</p>
</div>

<div class="step-box">
  <h4>Step 2: Fill in Entry Details</h4>
  <p>Fill in the entry details specific to the selected area.</p>
</div>

<h4>Filtering by Area</h4>
<div class="step-box">
  <h4>Step 1: Use Area Filter</h4>
  <p>On the Register page, use the "Area" filter to filter entries by ISQM area.</p>
</div>

<div class="step-box">
  <h4>Step 2: View Filtered Results</h4>
  <p>Select an area from the dropdown and click "Filter" to see only entries for that area.</p>
</div>


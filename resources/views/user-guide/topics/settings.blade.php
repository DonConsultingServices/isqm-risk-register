<h3>Accessing Settings</h3>
<div class="step-box">
  <h4>Step 1: Navigate to Management</h4>
  <p>Click on "Management" in the main navigation sidebar.</p>
</div>

<div class="step-box">
  <h4>Step 2: Click "Settings"</h4>
  <p>Click on "Settings" in the Management dropdown menu.</p>
</div>

<h3>Available Settings</h3>

<h4>Brand Settings</h4>
<p>Configure brand-related settings:
  <ul>
    <li><strong>Brand Color</strong> - Set the primary brand color for the system</li>
    <li><strong>Brand Name</strong> - Set the brand name displayed in the system</li>
    <li><strong>Logo</strong> - Upload a logo for the system (if supported)</li>
  </ul>
</p>

<h4>System Settings</h4>
<p>Configure system-wide settings:
  <ul>
    <li><strong>Default Status</strong> - Set the default status for new entries</li>
    <li><strong>Notification Settings</strong> - Configure notification preferences</li>
    <li><strong>Email Settings</strong> - Configure email notifications (if supported)</li>
  </ul>
</p>

<h4>Compliance Settings</h4>
<p>Configure compliance-related settings:
  <ul>
    <li><strong>Compliance Email Recipients</strong> - Set email addresses for compliance notifications</li>
    <li><strong>Compliance Teams Webhook</strong> - Configure webhook for Teams notifications (if supported)</li>
    <li><strong>Compliance Review Frequency</strong> - Set how often compliance reviews should be conducted</li>
  </ul>
</p>

<h3>Updating Settings</h3>
<div class="step-box">
  <h4>Step 1: Review Current Settings</h4>
  <p>Review the current settings to understand what's configured.</p>
</div>

<div class="step-box">
  <h4>Step 2: Update Settings</h4>
  <p>Modify the settings as needed. Enter new values or select new options from dropdowns.</p>
</div>

<div class="step-box">
  <h4>Step 3: Save Settings</h4>
  <p>Click the "Save" or "Update Settings" button to save your changes. You'll be asked to confirm before saving.</p>
</div>


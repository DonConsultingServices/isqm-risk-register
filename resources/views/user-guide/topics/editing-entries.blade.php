<h2>Editing ISQM Entries</h2>

<p>You can edit existing ISQM entries to update information, change status, or add new details. This guide explains how to edit entries effectively.</p>

<h3>Accessing the Edit Page</h3>
<div class="step-box">
  <h4>Step 1: Find the Entry</h4>
  <p>Navigate to the Register page and find the entry you want to edit. You can use filters to help locate it.</p>
</div>

<div class="step-box">
  <h4>Step 2: Click "Edit"</h4>
  <p>Click the "Edit" button next to the entry in the actions column. You'll be asked to confirm before proceeding.</p>
</div>

<div class="step-box">
  <h4>Alternative: View First</h4>
  <p>You can also click "View" to see the entry details first, then click "Edit" from the view page.</p>
</div>

<h3>Making Changes</h3>
<p>The edit form is similar to the create form, but it will be pre-filled with the current entry data. You can modify any field as needed.</p>

<div class="info-box">
  <h4>üí° Note</h4>
  <p>All fields can be edited, including required fields. Make sure to fill in all required fields before saving.</p>
</div>

<h3>Updating Entry Status</h3>
<div class="step-box">
  <h4>Changing Status</h4>
  <p>You can change the entry status:
    <ul>
      <li><strong>Open</strong> - Entry is active and requires attention</li>
      <li><strong>Monitoring</strong> - Entry is being monitored</li>
      <li><strong>Closed</strong> - Entry is completed or no longer applicable</li>
    </ul>
  </p>
</div>

<div class="warning-box">
  <h4>‚ö†Ô∏è Status Change Warning</h4>
  <p>When you change an entry's status to "Closed", it may no longer appear in certain filtered views. Make sure this is the correct action before saving.</p>
</div>

<h3>Adding Attachments</h3>
<div class="step-box">
  <h4>Upload Additional Files</h4>
  <p>You can add more files to an existing entry:
    <ol>
      <li>Scroll down to the "Attachments" section</li>
      <li>Click "Choose Files" or drag and drop files</li>
      <li>Select the files you want to upload</li>
      <li>Click "Update Entry" to save</li>
    </ol>
  </p>
</div>

<div class="step-box">
  <h4>Viewing Existing Attachments</h4>
  <p>Existing attachments will be displayed in the attachments section. You can click on them to view or download.</p>
</div>

<h3>Saving Changes</h3>
<div class="step-box">
  <h4>Step 1: Review Changes</h4>
  <p>Before saving, review all the changes you've made to ensure they're correct.</p>
</div>

<div class="step-box">
  <h4>Step 2: Click "Update Entry"</h4>
  <p>Click the "Update Entry" button at the bottom of the form. You'll be asked to confirm before saving.</p>
</div>

<div class="step-box">
  <h4>Step 3: Confirmation</h4>
  <p>After saving, you'll be redirected back to the previous page, and your changes will be saved.</p>
</div>

<h3>Cancel Editing</h3>
<div class="step-box">
  <h4>Cancel Button</h4>
  <p>If you want to cancel editing without saving changes, click the "Cancel" button. You'll be asked to confirm, and any unsaved changes will be lost.</p>
</div>

<div class="warning-box">
  <h4>‚ö†Ô∏è Unsaved Changes Warning</h4>
  <p>The system will warn you if you try to leave the page with unsaved changes. Make sure to save your work before leaving.</p>
</div>

<h3>Deleting Entries</h3>
<div class="step-box">
  <h4>Deleting an Entry</h4>
  <p>To delete an entry:
    <ol>
      <li>Navigate to the entry you want to delete</li>
      <li>Click the "Delete" button (red button)</li>
      <li>Confirm the deletion in the popup dialog</li>
    </ol>
  </p>
</div>

<div class="warning-box">
  <h4>‚ö†Ô∏è Delete Warning</h4>
  <p>Deleting an entry is permanent and cannot be undone. All associated data, attachments, and records will be permanently removed. Make sure you want to delete the entry before confirming.</p>
</div>

<h3>Activity Log</h3>
<div class="info-box">
  <h4>üí° Activity Tracking</h4>
  <p>All changes to entries are logged in the activity log. Administrators and managers can view the activity log to see who made what changes and when.</p>
</div>

<h3>Best Practices</h3>
<div class="tip-box">
  <h4>üí° Tips for Editing</h4>
  <ul>
    <li>Update entries regularly to keep information current</li>
    <li>Add notes in the "Findings" field when monitoring activities reveal new information</li>
    <li>Update the status when an entry moves to a new stage</li>
    <li>Set review dates to ensure entries are reviewed periodically</li>
    <li>Attach supporting documents to provide evidence and context</li>
    <li>Update due dates if deadlines change</li>
  </ul>
</div>


<h2>Using Filters</h2>

<p>Filters help you find specific entries in the ISQM Register quickly. This guide explains how to use filters effectively.</p>

<h3>Accessing Filters</h3>
<div class="step-box">
  <h4>Step 1: Navigate to Register</h4>
  <p>Click on "Register" in the main navigation sidebar.</p>
</div>

<div class="step-box">
  <h4>Step 2: View Filter Section</h4>
  <p>The filter section is located at the top of the Register page, above the entries table.</p>
</div>

<h3>Available Filters</h3>

<h4>Search Filter</h4>
<div class="step-box">
  <h4>Search</h4>
  <p>The search filter allows you to search across multiple fields:
    <ul>
      <li>Quality objectives</li>
      <li>Quality risks</li>
      <li>Findings</li>
      <li>Assessment of risk</li>
      <li>Response</li>
      <li>Firm implementation</li>
    </ul>
    Simply type your search term in the search box and click "Filter".
  </p>
</div>

<div class="tip-box">
  <h4>ðŸ’¡ Search Tips</h4>
  <ul>
    <li>The search is case-insensitive</li>
    <li>It searches for partial matches (e.g., "governance" will find "Governance & Leadership")</li>
    <li>You can search for multiple terms</li>
  </ul>
</div>

<h4>Area Filter</h4>
<div class="step-box">
  <h4>Area</h4>
  <p>Filter entries by ISQM area:
    <ul>
      <li>Governance and leadership</li>
      <li>Ethical requirements</li>
      <li>Acceptance and continuance</li>
      <li>Engagement performance</li>
      <li>Resources</li>
      <li>Information and communication</li>
      <li>All Areas (default)</li>
    </ul>
    Select an area from the dropdown and click "Filter".
  </p>
</div>

<h4>Status Filter</h4>
<div class="step-box">
  <h4>Status</h4>
  <p>Filter entries by status:
    <ul>
      <li><strong>Open</strong> - Active entries requiring attention</li>
      <li><strong>Monitoring</strong> - Entries being monitored</li>
      <li><strong>Closed</strong> - Completed or no longer applicable entries</li>
      <li>All Status (default)</li>
    </ul>
  </p>
</div>

<h4>Client Filter</h4>
<div class="step-box">
  <h4>Client</h4>
  <p>Filter entries by associated client. Select a client from the dropdown to see only entries associated with that client. Select "All Clients" to see all entries.</p>
</div>

<h3>Applying Filters</h3>
<div class="step-box">
  <h4>Step 1: Set Filter Values</h4>
  <p>Select or enter values for the filters you want to use. You can use multiple filters at once.</p>
</div>

<div class="step-box">
  <h4>Step 2: Click "Filter"</h4>
  <p>Click the "Filter" button to apply the filters. The entries table will update to show only matching entries.</p>
</div>

<div class="step-box">
  <h4>Step 3: View Results</h4>
  <p>The filtered results will be displayed in the table below. The number of results will be shown at the bottom of the table.</p>
</div>

<h3>Clearing Filters</h3>
<div class="step-box">
  <h4>Clear All Filters</h4>
  <p>Click the "Clear" button to reset all filters and show all entries again.</p>
</div>

<h3>Filtering on ISQM Area Pages</h3>
<p>When viewing a specific ISQM area page (e.g., Governance, Ethical Requirements), you have access to more detailed filters:</p>

<ul>
  <li><strong>Quality Objective</strong> - Filter by specific quality objectives</li>
  <li><strong>Quality Risk</strong> - Filter by quality risks</li>
  <li><strong>Assessment of Risk</strong> - Filter by risk assessments</li>
  <li><strong>Likelihood</strong> - Filter by likelihood (Yes/No)</li>
  <li><strong>Adverse Effect</strong> - Filter by adverse effect (Yes/No)</li>
  <li><strong>Risk Applicable</strong> - Filter by risk applicability (Yes/No)</li>
  <li><strong>Response</strong> - Filter by response types</li>
  <li><strong>Firm Implementation</strong> - Filter by firm implementation</li>
  <li><strong>TOC</strong> - Filter by test of controls</li>
  <li><strong>Monitoring Activity</strong> - Filter by monitoring activities</li>
  <li><strong>Findings</strong> - Filter by findings</li>
  <li><strong>Deficiency Type</strong> - Filter by deficiency types</li>
  <li><strong>Status</strong> - Filter by entry status</li>
  <li><strong>Due Date</strong> - Filter by due date range</li>
  <li><strong>Review Date</strong> - Filter by review date</li>
  <li><strong>Owner</strong> - Filter by entry owner</li>
  <li><strong>Client</strong> - Filter by client</li>
</ul>

<h3>Advanced Filtering Tips</h3>
<div class="tip-box">
  <h4>ðŸ’¡ Tips for Effective Filtering</h4>
  <ul>
    <li>Use multiple filters together to narrow down results</li>
    <li>Combine search with area filter to find specific entries quickly</li>
    <li>Use status filter to see only active entries (Open or Monitoring)</li>
    <li>Filter by client to see all entries for a specific client</li>
    <li>Use date filters to find entries due soon or overdue</li>
    <li>Clear filters regularly to avoid confusion</li>
  </ul>
</div>

<h3>Filtering Best Practices</h3>
<div class="info-box">
  <h4>ðŸ’¡ Best Practices</h4>
  <ul>
    <li>Start with broad filters (area, status) and then narrow down</li>
    <li>Use search for quick lookup when you know specific keywords</li>
    <li>Save common filter combinations by bookmarking the URL</li>
    <li>Clear filters before starting a new search</li>
    <li>Use filters on area pages for more detailed filtering options</li>
  </ul>
</div>


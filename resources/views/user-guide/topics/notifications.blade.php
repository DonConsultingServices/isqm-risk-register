<h2>Notifications</h2>

<p>The Notifications system keeps you informed about important events and updates in the ISQM system. This guide explains how to use notifications.</p>

<h3>Accessing Notifications</h3>
<div class="step-box">
  <h4>Step 1: Navigate to Notifications</h4>
  <p>Click on "Notifications" in the main navigation sidebar, or click the notification bell icon if available.</p>
</div>

<h3>Viewing Notifications</h3>
<div class="step-box">
  <h4>Notification List</h4>
  <p>The Notifications page displays a list of all notifications, showing:
    <ul>
      <li>Notification message</li>
      <li>Timestamp</li>
      <li>Read/Unread status</li>
      <li>Actions (Mark as Read, Delete)</li>
    </ul>
  </p>
</div>

<h3>Types of Notifications</h3>
<p>The system may send notifications for:
  <ul>
    <li><strong>Entry Updates</strong> - When entries are created, updated, or deleted</li>
    <li><strong>Due Date Reminders</strong> - When entries are approaching their due date</li>
    <li><strong>Overdue Entries</strong> - When entries are past their due date</li>
    <li><strong>Status Changes</strong> - When entry status changes</li>
    <li><strong>Assignment Changes</strong> - When entries are assigned to you</li>
    <li><strong>System Updates</strong> - When system settings or configurations change</li>
  </ul>
</p>

<h3>Managing Notifications</h3>

<h4>Marking Notifications as Read</h4>
<div class="step-box">
  <h4>Step 1: View Notification</h4>
  <p>Click on a notification to view its details and mark it as read.</p>
</div>

<div class="step-box">
  <h4>Step 2: Mark as Read</h4>
  <p>Click the "Mark as Read" button to mark the notification as read. Read notifications will be visually distinguished from unread notifications.</p>
</div>

<h4>Marking All as Read</h4>
<div class="step-box">
  <h4>Mark All as Read</h4>
  <p>Click the "Mark All as Read" button to mark all notifications as read at once.</p>
</div>

<h4>Deleting Notifications</h4>
<div class="step-box">
  <h4>Delete Notification</h4>
  <p>Click the "Delete" button next to a notification to delete it. Deleted notifications cannot be recovered.</p>
</div>


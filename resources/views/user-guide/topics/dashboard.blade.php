<h2>Dashboard Overview</h2>

<p>The Dashboard provides an overview of your ISQM entries and key metrics. This guide explains how to use the Dashboard effectively.</p>

<h3>Accessing the Dashboard</h3>
<div class="step-box">
  <h4>Step 1: Log In</h4>
  <p>Log in to the system using your credentials.</p>
</div>

<div class="step-box">
  <h4>Step 2: View Dashboard</h4>
  <p>After logging in, you'll be redirected to the Dashboard. You can also access it by clicking "Dashboard" in the main navigation sidebar.</p>
</div>

<h3>Dashboard Components</h3>

<h4>Summary Statistics</h4>
<p>The Dashboard displays key statistics about your ISQM entries:</p>
<ul>
  <li><strong>Total Entries</strong> - Total number of ISQM entries in the system</li>
  <li><strong>Open Entries</strong> - Number of entries with "Open" status</li>
  <li><strong>Monitoring Entries</strong> - Number of entries with "Monitoring" status</li>
  <li><strong>Closed Entries</strong> - Number of entries with "Closed" status</li>
  <li><strong>Overdue Entries</strong> - Number of entries past their due date</li>
</ul>

<h4>Area Breakdown</h4>
<p>The Dashboard may show a breakdown of entries by ISQM area:
  <ul>
    <li>Governance & Leadership</li>
    <li>Ethical Requirements</li>
    <li>Acceptance & Continuance</li>
    <li>Engagement Performance</li>
    <li>Resources</li>
    <li>Information & Communication</li>
  </ul>
</p>

<h4>Recent Entries</h4>
<p>The Dashboard may display recent entries or entries that need attention, such as:
  <ul>
    <li>Recently created entries</li>
    <li>Entries that are overdue</li>
    <li>Entries requiring review</li>
  </ul>
</p>

<h3>Using the Dashboard</h3>

<h4>Quick Navigation</h4>
<div class="step-box">
  <h4>Navigate to Register</h4>
  <p>Click on "Register" in the navigation sidebar to view all ISQM entries.</p>
</div>

<div class="step-box">
  <h4>Navigate to Compliance Now</h4>
  <p>Click on "Compliance Now" to view entries that require immediate attention.</p>
</div>

<div class="step-box">
  <h4>Navigate to Reports</h4>
  <p>Click on "Reports" in the Management menu to generate reports and export data.</p>
</div>


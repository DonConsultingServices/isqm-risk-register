<h2>Reports & Exports</h2>

<p>The Reports & Exports feature allows you to generate reports and export ISQM entry data for analysis and documentation. This guide explains how to use this feature.</p>

<h3>Accessing Reports</h3>
<div class="step-box">
  <h4>Step 1: Navigate to Management</h4>
  <p>Click on "Management" in the main navigation sidebar.</p>
</div>

<div class="step-box">
  <h4>Step 2: Click "Reports"</h4>
  <p>Click on "Reports" in the Management dropdown menu.</p>
</div>

<h3>Available Export Options</h3>

<h4>CSV Export</h4>
<div class="step-box">
  <h4>Export to CSV</h4>
  <p>Export ISQM entries to a CSV file:
    <ol>
      <li>Navigate to the Reports page</li>
      <li>Click the "Export CSV" button</li>
      <li>The CSV file will be downloaded to your computer</li>
      <li>Open the file in Excel or another spreadsheet application</li>
    </ol>
  </p>
</div>

<div class="info-box">
  <h4>ðŸ’¡ CSV Export Note</h4>
  <p>The CSV export includes all ISQM entries in the system. You can filter entries before exporting by using the filters on the Register page.</p>
</div>

<h4>Excel Export</h4>
<div class="step-box">
  <h4>Export to Excel</h4>
  <p>Export ISQM entries to an Excel file:
    <ol>
      <li>Navigate to the Reports page</li>
      <li>Click the "Export Excel" button</li>
      <li>The Excel file will be downloaded to your computer</li>
      <li>Open the file in Excel or another spreadsheet application</li>
    </ol>
  </p>
</div>

<div class="tip-box">
  <h4>ðŸ’¡ Excel Export Tips</h4>
  <ul>
    <li>Excel files are easier to work with than CSV files</li>
    <li>Excel files preserve formatting and can include multiple sheets</li>
    <li>You can use Excel formulas and pivot tables to analyze the data</li>
  </ul>
</div>

<h4>Compliance Export</h4>
<div class="step-box">
  <h4>Export Compliance Data</h4>
  <p>Export compliance-specific data:
    <ol>
      <li>Navigate to the Compliance Now page</li>
      <li>Click the "Download CSV" button</li>
      <li>The CSV file will contain only entries relevant for compliance</li>
      <li>Open the file in Excel or another spreadsheet application</li>
    </ol>
  </p>
</div>

<div class="info-box">
  <h4>ðŸ’¡ Compliance Export Note</h4>
  <p>The compliance export includes only entries where "Risk Applicable" is set to "Yes" or is not yet determined (NULL). Entries where "Risk Applicable" is "No" are excluded.</p>
</div>

<h3>Exporting Filtered Data</h3>
<div class="step-box">
  <h4>Step 1: Apply Filters</h4>
  <p>Use filters on the Register page to filter entries by area, status, client, or other criteria.</p>
</div>

<div class="step-box">
  <h4>Step 2: Export Filtered Results</h4>
  <p>After applying filters, navigate to the Reports page and export the data. The export will include only the filtered entries.</p>
</div>

<div class="tip-box">
  <h4>ðŸ’¡ Filtering Tips</h4>
  <ul>
    <li>Filter by area to export entries for a specific ISQM area</li>
    <li>Filter by status to export only open, monitoring, or closed entries</li>
    <li>Filter by client to export entries for a specific client</li>
    <li>Use date filters to export entries within a specific date range</li>
  </ul>
</div>

<h3>Using Exported Data</h3>

<h4>Analysis</h4>
<p>You can use exported data for:
  <ul>
    <li>Analyzing entry trends over time</li>
    <li>Identifying areas that need attention</li>
    <li>Tracking compliance status</li>
    <li>Generating custom reports</li>
    <li>Sharing data with stakeholders</li>
  </ul>
</p>

<h4>Documentation</h4>
<p>Exported data can be used for:
  <ul>
    <li>Compliance documentation</li>
    <li>Audit reports</li>
    <li>Quality management reports</li>
    <li>Executive summaries</li>
    <li>Regulatory filings</li>
  </ul>
</p>

<h3>Best Practices</h3>
<div class="tip-box">
  <h4>ðŸ’¡ Best Practices</h4>
  <ul>
    <li>Export data regularly to maintain backups</li>
    <li>Use filters to export specific subsets of data</li>
    <li>Name exported files with descriptive names and dates</li>

<h3>Navigation</h3>
<p>Once logged in, you'll see the main navigation sidebar with the following sections:</p>
<ul>
  <li><strong>Dashboard</strong> - Overview of your ISQM entries and key metrics</li>
  <li><strong>Register</strong> - Main ISQM register where you can view, create, and manage entries</li>
  <li><strong>ISQM Areas</strong> - Access specific ISQM area pages (Governance, Ethical, etc.)</li>
  <li><strong>Management</strong> - Manage clients, risks, reports, activity logs, users, and settings</li>
</ul>

<h3>User Roles</h3>
<p>The system has three user roles with different access levels:</p>
<ul>
  <li><strong>Staff</strong> - Can view and create ISQM entries, manage their own entries</li>
  <li><strong>Manager</strong> - Can manage clients, monitoring activities, and view activity logs</li>
  <li><strong>Admin</strong> - Full access to all features including user management and settings</li>
</ul>


<h2>Creating ISQM Entries</h2>

<p>ISQM entries are the core of the system. Each entry represents a quality objective, risk, and response. This guide will walk you through creating a new entry.</p>

<h3>Accessing the Create Page</h3>
<div class="step-box">
  <h4>Step 1: Navigate to the Register</h4>
  <p>Click on "Register" in the main navigation sidebar.</p>
</div>

<div class="step-box">
  <h4>Step 2: Click "Add Entry"</h4>
  <p>Click the "Add Entry" button in the top right corner of the Register page.</p>
</div>

<h3>Filling Out the Entry Form</h3>

<h4>Basic Information</h4>
<div class="step-box">
  <h4>Area (Required)</h4>
  <p>Select the ISQM area this entry belongs to:
    <ul>
      <li>Governance and leadership</li>
      <li>Ethical requirements</li>
      <li>Acceptance and continuance</li>
      <li>Engagement performance</li>
      <li>Resources</li>
      <li>Information and communication</li>
    </ul>
  </p>
</div>

<div class="step-box">
  <h4>Status (Required)</h4>
  <p>Select the current status:
    <ul>
      <li><strong>Open</strong> - Entry is active and requires attention</li>
      <li><strong>Monitoring</strong> - Entry is being monitored</li>
      <li><strong>Closed</strong> - Entry is completed or no longer applicable</li>
    </ul>
  </p>
</div>

<h4>Quality Objective</h4>
<div class="step-box">
  <h4>Quality Objective (Required)</h4>
  <p>Enter a clear description of the quality objective. This should describe what quality standard or requirement this entry addresses.</p>
</div>

<div class="step-box">
  <h4>Quality Risk (Optional)</h4>
  <p>Describe the quality risk associated with this objective. What could go wrong if this objective is not met?</p>
</div>

<div class="step-box">
  <h4>Assessment of Risk (Optional)</h4>
  <p>Provide an assessment of the risk, including its potential impact and likelihood.</p>
</div>

<h4>Risk Flags</h4>
<div class="step-box">
  <h4>Risk Flags</h4>
  <p>Check the appropriate boxes:
    <ul>
      <li><strong>Likelihood</strong> - Is this risk likely to occur?</li>
      <li><strong>Severe</strong> - Is this a severe risk?</li>
      <li><strong>Pervasive</strong> - Is this risk pervasive across the organization?</li>
      <li><strong>Adverse Effect</strong> - Does this risk have an adverse effect?</li>
      <li><strong>Risk Applicable</strong> - Is this risk applicable to your organization?</li>
    </ul>
  </p>
</div>

<h4>Response and Implementation</h4>
<div class="step-box">
  <h4>Response to Quality Risk (Optional)</h4>
  <p>Describe how your organization responds to this quality risk. What actions are taken to mitigate or manage the risk?</p>
</div>

<div class="step-box">
  <h4>Firm Implementation (Optional)</h4>
  <p>Describe how the firm implements the response. What processes or procedures are in place?</p>
</div>

<div class="step-box">
  <h4>TOC - Test of Control (Optional)</h4>
  <p>Describe the test of controls performed to verify that the response is effective.</p>
</div>

<h4>Monitoring and Findings</h4>
<div class="step-box">
  <h4>Monitoring Activity (Optional)</h4>
  <p>Select a monitoring activity from the dropdown, or create a new one if needed (Manager/Admin only).</p>
</div>

<div class="step-box">
  <h4>Findings (Optional)</h4>
  <p>Document any findings from monitoring activities. What was discovered during the monitoring process?</p>
</div>

<div class="step-box">
  <h4>Deficiency Type (Optional)</h4>
  <p>Select the type of deficiency found, if any. You can create new deficiency types if needed (Manager/Admin only).</p>
</div>

<div class="step-box">
  <h4>Root Cause (Optional)</h4>
  <p>If a deficiency was found, document the root cause of the deficiency.</p>
</div>

<div class="step-box">
  <h4>Remedial Actions (Optional)</h4>
  <p>Describe the remedial actions taken to address any deficiencies or risks.</p>
</div>

<h4>Assignment and Dates</h4>
<div class="step-box">
  <h4>Owner (Required)</h4>
  <p>Select the user responsible for this entry. This person will be responsible for managing and updating the entry.</p>
</div>

<div class="step-box">
  <h4>Client (Optional)</h4>
  <p>If this entry is associated with a specific client, select the client from the dropdown. You can create new clients if needed (Manager/Admin only).</p>
</div>

<div class="step-box">
  <h4>Due Date (Optional)</h4>
  <p>Set a due date for when this entry should be reviewed or completed. Entries past their due date will be marked as overdue.</p>
</div>

<div class="step-box">
  <h4>Review Date (Optional)</h4>
  <p>Set a review date for when this entry should be reviewed again.</p>
</div>

<h4>Attachments</h4>
<div class="step-box">
  <h4>Upload Files (Optional)</h4>
  <p>You can attach files to the entry, such as documents, images, or spreadsheets. Supported formats include PDF, Word, Excel, and images.</p>
</div>

<h3>Saving the Entry</h3>
<div class="step-box">
  <h4>Step 1: Review Your Entry</h4>
  <p>Before saving, review all the information you've entered to ensure it's accurate and complete.</p>
</div>

<div class="step-box">
  <h4>Step 2: Click "Save Entry"</h4>
  <p>Click the "Save Entry" button at the bottom of the form. You'll be asked to confirm before saving.</p>
</div>

<div class="step-box">
  <h4>Step 3: Confirmation</h4>
  <p>After saving, you'll be redirected back to the Register page, and your new entry will appear in the list.</p>
</div>

<div class="warning-box">
  <h4>‚ö†Ô∏è Important Notes</h4>
  <ul>
    <li>Some fields are required (marked with *) and must be filled in before saving</li>
    <li>You can cancel at any time, but any unsaved data will be lost</li>
    <li>The system will warn you before leaving the page if you have unsaved changes</li>
  </ul>
</div>

<div class="tip-box">
  <h4>üí° Tips</h4>
  <ul>
    <li>Fill in as much information as possible to make the entry more useful</li>
    <li>Set due dates to help track when entries need to be reviewed</li>
    <li>Attach relevant documents to provide context and evidence</li>
    <li>Use the "Risk Applicable" field to filter entries in the Compliance Now page</li>
  </ul>
</div>

